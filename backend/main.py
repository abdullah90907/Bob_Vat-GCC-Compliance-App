@app.post("/api/ask", response_model=AskResponse)
async def ask_question(request: AskRequest):
    """
    Answer natural language questions about VAT
    
    This endpoint:
    1. Auto-detects language from question text
    2. Searches for relevant information using Tavily
    3. Uses Qwen to answer in detected language
    4. Formats response using Bob service
    """
    try:
        logger.info(f"Question received: {request.question[:100]}...")
        
        # Detect language
        detected_language = request.language
        if not detected_language:
            detected_language = await qwen_service.detect_language(request.question)
        
        # Build GCC data context
        gcc_context = "\n".join([
            f"{country}: {data['vat_rate']}% VAT, Status: {data['status']}, "
            f"Threshold: {data.get('mandatory_threshold', 'N/A')}"
            for country, data in GCC_VAT_DATA.items()
        ])
        
        # Search for relevant information
        search_query = request.question
        if request.country:
            search_query = f"{request.country} {request.question}"
        
        search_results = await tavily_service.search(search_query, max_results=5)
        context = tavily_service.format_results_for_context(search_results)
        
        # Get answer from Qwen
        answer_result = await qwen_service.answer_question(
            question=request.question,
            context=context,
            gcc_data=gcc_context,
            detected_language=detected_language
        )
        
        if not answer_result["success"]:
            raise HTTPException(
                status_code=status.HTTP_503_SERVICE_UNAVAILABLE,
                detail="Question answering service temporarily unavailable"
            )
        
        # Extract sources from search results
        sources = []
        if search_results.get("success"):
            for result in search_results.get("results", [])[:5]:
                sources.append(Source(
                    title=result.get("title", ""),
                    url=result.get("url"),
                    date=result.get("published_date"),
                    snippet=result.get("content", "")[:200]
                ))
        
        # Format response using Bob service
        response = bob_service.format_ask_response(
            answer=answer_result["answer"],
            detected_language=detected_language,
            sources=[s.model_dump() for s in sources],
            live_search=search_results.get("success", False)
        )
        
        return AskResponse(**response)
    
    except HTTPException:
        raise
    except Exception as e:
        logger.error(f"Error in ask_question: {str(e)}", exc_info=True)
        raise HTTPException(
            status_code=status.HTTP_500_INTERNAL_SERVER_ERROR,
            detail="Failed to answer question"
        )


